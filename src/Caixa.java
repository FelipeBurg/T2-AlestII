public class Caixa{
    private int largura;
    private int altura;
    private int comprimento;

    public Caixa(int largura, int altura, int comprimento){
        this.largura = largura;
        this.altura = altura;
        this.comprimento = comprimento;
    }

    public int getLargura(){
        return largura;
    }
    public int getAltura(){
        return altura;
    }

    public int getComprimento(){
        return comprimento;
    }

}
